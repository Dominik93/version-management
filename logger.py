

def log(logMessage):
	print(logMessage)

def specialLog(logMessage):
	print('*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*')
	print(logMessage)
	print('*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*')

def commandLog(logMessage):
	print('>>>>>>>>>>     ' + logMessage)

def prettyLog(logMessage):
	print('------------------------------------------------------------------------------------')
	print(logMessage)
	print('------------------------------------------------------------------------------------')